void clock_init()	//クロックを変更
{
	
	CPG.FRQCR.BIT.IFC    = 1;       	// Iφ = 50MHz		内部クロック
    CPG.FRQCR.BIT.BFC    = 3;        	// Bφ = 25MHz		バスクロック
    CPG.FRQCR.BIT._PFC   = 3;        	// Pφ = 25MHz		周辺クロック
    CPG.FRQCR.BIT.MPFC   = 3;        	// MPφ = 25MHz		MTU2 クロック

}

